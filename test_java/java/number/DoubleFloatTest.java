package number;

/**
 * Java中浮点型默认是double类型的，整型默认是int类型的
 */
public class DoubleFloatTest {
    public static void main(String[] args) {

        // float a = 1.5;
        float a1 = 1.5f;
        float a2 = (float) 1.5;

        byte b1 = 10;
        short b2 = 10;
        int b3 = 10;
        long b4 = 10;
    }
}
